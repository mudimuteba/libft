#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char *ptr_s1;

	ptr_s1 = s1;
	while (*s1)
		s1++;
	while ((*s1++ = *s2++))
		;
	return (ptr_s1);
}
